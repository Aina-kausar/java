public class Expressions {
    public static void main(String args[]) {
        int a = (-5) + 8 * 6;
        int b = (55 + 9) % 9;
        float c = 20 + (-3) * 5 / 8;
        float d = 5 + 15 / (3 * 2) - 8 % 3;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
